def get_FR_model(imgsize=[112, 112], arcface_path=''):

    model = IR_101(imgsize)
    if not os.path.exists(arcface_path):
        raise NotImplementedError(arcface_path)

    checkpoint = torch.load(
        arcface_path, map_location=lambda storage, loc: storage)
    checkpoint_no_module = {}
    for k, v in checkpoint.items():
        if k.startswith('module'):
            k = k[7:]
        checkpoint_no_module[k] = v

    model.load_state_dict(checkpoint_no_module)
    print(f"[Load ArcFace] from {arcface_path}")

    model = model.eval()
    for param in model.parameters():
        param.requires_grad = False
    return model


